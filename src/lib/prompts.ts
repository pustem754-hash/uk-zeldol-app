/**
 * Получить промпт для SEO-генерации
 */
export function getSeoPrompt(type: 'text' | 'meta' | 'h1' | 'product'): string {
  switch (type) {
    case 'text':
      return SEO_SYSTEM_PROMPT;
    case 'meta':
      return META_DESCRIPTION_PROMPT;
    case 'h1':
      return H1_PROMPT;
    case 'product':
      return PRODUCT_DESCRIPTION_PROMPT;
    default:
      return GENERAL_CHAT_PROMPT;
  }
}

/**
 * Получить общий промпт для чата
 */
export function getGeneralPrompt(): string {
  return GENERAL_CHAT_PROMPT;
}
